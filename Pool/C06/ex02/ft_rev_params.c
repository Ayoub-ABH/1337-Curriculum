#include <unistd.h>

int	main(int ac, char **av)
{
	int	j;

	j = 0;
	ac--;
	while (ac != 0)
	{
		j = 0;
		while (av[ac][j])
		{
			write(1, &av[ac][j], 1);
			j++;
		}
		write(1, "\n", 1);
		ac--;
	}
	return (0);
}
